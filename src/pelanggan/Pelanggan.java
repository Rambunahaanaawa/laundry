    public void tampilkanData(){
        DefaultTableModel pelanggan = new DefaultTableModel();
        pelanggan.addColumn("ID Pelanggan");
        pelanggan.addColumn("Nama Pelanggan");
        pelanggan.addColumn("Alamat Pelanggan");
        pelanggan.addColumn("Jenis Laundry");
       
        jTable1.setModel(pelanggan);
        
        
        try {
            Statement stat = koneks.createStatement (); 
            ResultSet data = stat.executeQuery("SELECT * FROM pelanggan"); 
            while (data.next())  {
                
                pelanggan.addRow (new Object[]   {
                    data.getString("id_pelanggan"),
                    data.getString("nama_pelanggan"),
                    data.getString("alamat_pelanggan"),
                    data.getString("jenis_laundry"),
                });
                
                jTable1.setModel(pelanggan);
                }
        } catch (SQLException e) {
            System.err.println("Terjadi Kesalahan :" + e);
        }
        
    }
   
