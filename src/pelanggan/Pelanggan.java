    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
         Statement stat = koneks.createStatement(); 
                
                ResultSet data = stat.executeQuery("SELECT * FROM pelanggan WHERE id_pelanggan='" + jTextField1.getText() + "'");
                if (data.next())
                    {
                    
                    JOptionPane.showMessageDialog (null, "ID Pelanggan sudah ada!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                    jComboBox1.requestFocus(); 
                } 
                else if (jTextField1.getText().equals("") || jTextField2.getText().equals("") ||
                        jTextField3.getText().equals("") || jComboBox1.getSelectedItem().equals("")) 
                {
                    JOptionPane.showMessageDialog (null, "Data Tidak Boleh Kosong!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                
                    String sql = "INSERT INTO pelanggan VALUES ('" + jTextField1.getText() + "'"
                            + ",'" + jTextField2.getText() + "'"
                            + ",'" + jTextField3.getText() + "'"
                            + ",'" + jComboBox1.getSelectedItem() + "')";
                    stat.executeUpdate(sql); 
                    
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jComboBox1.setSelectedItem("");
                    jTextField1.requestFocus(); 
                   
                    JOptionPane.showMessageDialog (null, "Data Berhasil DiSimpan!!!", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                    tampilkanData(); 
                }
                stat.close();
            }
        catch (Exception exc)
        {
            System.err.println("Terjadi Kesalahan :" + exc);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
      int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin mengubah data ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0)
        {
            try
            {
                
                String sql ="UPDATE pelanggan SET nama_pelanggan = '"+jTextField2.getText()
                        +"', alamat_pelanggan = '"+jTextField3.getText()
                        +"', jenis_laundry = '"+jComboBox1.getSelectedItem()
                        +"' WHERE id_pelanggan='" + jTextField1.getText().trim() + "'";
                
                try (Statement stat = koneks.createStatement()) {
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah!", "Ubah Data", JOptionPane.INFORMATION_MESSAGE);
                }
                
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jComboBox1.setSelectedItem("");
                jTextField1.enable(true);
                jTextField1.requestFocus();
                tampilkanData();
            }
            catch (HeadlessException | SQLException e)
            {
                JOptionPane.showMessageDialog(null, "Perubahan data Gagal"+e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

